function material({
  id,
  group,
  section = "",
  subcategory = section,
  name,
  dimensions = [],
  lengths = [],
  coveringWidth = [],
  coverage = [],
  description = {},
  thicknesses = [],
  sheetSizes = [],
  packages = [],
  profile = "rect",
  mounting = "",
  source = "lathunden",
  status = "complete",
}) {
  const isStoreScan = source === "storeScan";
  const image = isStoreScan ? storeScanImages[id] : cardCropImages[id];

  return {
    id,
    group,
    section,
    subcategory,
    category: name.sv,
    name,
    dimensions,
    lengths,
    coveringWidth,
    coverage,
    description,
    thicknesses,
    sheetSizes,
    packages,
    profile,
    mounting,
    source,
    sourceImage: isStoreScan ? storeScanSourceImage : lathundenImages[id] || "",
    combinedImage: image || "",
    drawing2d: "",
    render3d: "",
    status,
  };
}

