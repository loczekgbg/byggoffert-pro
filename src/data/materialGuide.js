function material({
  id,
  group,
  section = "",
  name,
  dimensions = [],
  lengths = [],
  coveringWidth = [],
  coverage = [],
  description = {},
  thicknesses = [],
  sheetSizes = [],
  packages = [],
  profile = "rect",
  mounting = "",
  status = "complete",
}) {
  return {
    id,
    group,
    section,
    category: name.sv,
    name,
    dimensions,
    lengths,
    coveringWidth,
    coverage,
    description,
    thicknesses,
    sheetSizes,
    packages,
    profile,
    mounting,
    lathundenImage: lathundenImages[id] || "",
    status,
  };
}

